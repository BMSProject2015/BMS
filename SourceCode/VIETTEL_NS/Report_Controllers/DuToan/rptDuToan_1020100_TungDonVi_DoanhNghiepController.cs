        public static DataTable DT_rptDuToan_1020100_TungDonVi_DoanhNghiep(String MaND,String iID_MaDonVi)
        {
            String DKDonVi = "", DKPhongBan = "", DK = "";
            SqlCommand cmd = new SqlCommand();
            DKDonVi = ThuNopModels.DKDonVi(MaND, cmd);
            DKPhongBan = ThuNopModels.DKPhongBan_QuyetToan(MaND, cmd);
            int DVT = 1000;
            String iID_MaPhongBanQuanLy = DonViModels.getPhongBanCuaDonVi(iID_MaDonVi);
            String SQL = String.Format(@"SELECT sLNS,sL,sK,sM,sTM,sTTM,sNG,sMoTa,iID_MaDonVi,sTenDonVi,SUM(rTuChi) as rTuChi,SUM(rHienVat) as rHienVat,SUM(rChiTapTrung) as rChiTapTrung
FROM
(
SELECT sLNS,sL,sK,sM,sTM,sTTM,sNG,sMoTa,iID_MaDonVi,sTenDonVi
,rTuChi=SUM(rTuChi/{0})
,rHienVat=SUM(rHienVat/{0})
,rChiTapChung=SUM(CASE WHEN iID_MaPhongBan='06' THEN rTuChi/{3}	ELSE rChiTapTrung/{3} END)
 FROM DT_ChungTuChiTiet
 WHERE iTrangThai=1  AND (sLNS='1020100' OR sLNS='1020000') 
 AND iID_MaDonVi =@iID_MaDonVi AND iNamLamViec=@iNamLamViec AND iID_MaPhongBan<>@MaPhongBanQL
  {1} {2}
 GROUP BY sLNS,sL,sK,sM,sTM,sTTM,sNG,sMoTa,iID_MaDonVi,sTenDonVi
 HAVING SUM(rTuChi/{0})<>0 OR SUM(rHienVat/{0})<>0 OR SUM(rChiTapTrung/{0})<>0
 UNION 
 SELECT sLNS,sL,sK,sM,sTM,sTTM,sNG,sMoTa,iID_MaDonVi,sTenDonVi
,rTuChi=SUM(rTuChi/{0})
,rHienVat=SUM(rHienVat/{0})
,rChiTapTrung=SUM(rChiTapTrung/{0})
 FROM DT_ChungTuChiTiet_PhanCap
 WHERE iTrangThai=1  AND (sLNS='1020100' OR sLNS='1020000') AND 0=1
 AND iID_MaDonVi =@iID_MaDonVi AND iNamLamViec=@iNamLamViec
  {1} {2}
 GROUP BY sLNS,sL,sK,sM,sTM,sTTM,sNG,sMoTa,iID_MaDonVi,sTenDonVi
HAVING SUM(rTuChi/{0})<>0 OR SUM(rHienVat/{0})<>0 OR SUM(rChiTapTrung/{0})<>0) a
GROUP BY sLNS,sL,sK,sM,sTM,sTTM,sNG,sMoTa,iID_MaDonVi,sTenDonVi
 ORDER BY sM,sTM,sTTM,sNG", DVT, DKPhongBan, DKDonVi);
            cmd.CommandText = SQL;
            cmd.Parameters.AddWithValue("@iNamLamViec", ReportModels.LayNamLamViec(MaND));
            cmd.Parameters.AddWithValue("@iID_MaDonVi", iID_MaDonVi);
            cmd.Parameters.AddWithValue("@MaPhongBanQL", iID_MaPhongBanQuanLy);
            DataTable dt = Connection.GetDataTable(cmd);
            cmd.Dispose();
            return dt;
        }
        public static String LayMoTa(String sLNS)
        {
            String sMoTa = "";

            String SQL = String.Format(@"SELECT sMoTa FROM NS_MucLucNganSach WHERE iTrangThai=1 AND sLNS={0}", sLNS);
            sMoTa = Connection.GetValueString(SQL, "");
            return sMoTa;
        }
